public extension UIColor {
  
  /// App Theme color
  class var appTheme: UIColor {
    get { return UIColor.init(red: 229/255, green: 112/255, blue: 117/255, alpha: 1.0) }
  }
  
  /// App Theme color gradient
  class var appTheme2: UIColor {
    get { return UIColor.init(red: 209/255, green: 80/255, blue: 127/255, alpha: 1.0) }
  }
  
}
